#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn locale_storage_round_trip() {
        for locale in [
            Locale::English,
            Locale::SimplifiedChinese,
            Locale::TraditionalChinese,
        ] {
            assert_eq!(Locale::from_storage(locale.storage_name()), Some(locale));
            assert!(!locale.text(TextKey::Settings).is_empty());
        }
    }

    #[test]
    fn chinese_item_counts_do_not_use_english_plural_rules() {
        assert_eq!(Locale::SimplifiedChinese.item_count(2), "2 个项目");
        assert_eq!(Locale::TraditionalChinese.item_count(1), "1 個項目");
    }
}
